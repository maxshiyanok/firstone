class Witter{
   constructor(posts,user){
      this._author = user.author
      this._postList = new PostList(posts);
      this._show = new View();
      this._show.viewName(user.author);
      this._show.viewAva(user);
   }
   showFeed(count = this._postList._posts.length, filt = {}){
      this._show.viewFeed(this._postList,count, filt,this._author);
   }
   showFiltTags(){
      this._show.viewFiltHashtags(this._postList);
   }
   showAuthTags(){
      this._show.viewFiltAuthors(this._postList);
   }
    addPost(post){
      this._postList.add(post);
      this.showFeed(10,{},this._author);
    }
    deletePost(id){
      this._postList.remove(id);
      this.showFeed(10,{},this._author);
    }
    editPost(id, post){
      this._postList.edit(id, post);
      this.showFeed(10,{},this._author);
   }
}
